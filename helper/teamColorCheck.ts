export function teamColorCheck(team: string) {
  if (team === 'Fehér csapat') {
    return '#FFFFFF';
  } else if (team === 'Kék csapat') {
    return '#1434A4';
  } else if (team === 'Piros csapat') {
    return '#D22B2B';
  } else if (team === 'Sárga csapat') {
    return '#FAFA33';
  } else if (team === 'Narancs csapat') {
    return '#FFA500';
  } else if (team === 'Lila csapat') {
    return '#800080';
  } else if (team === 'Rózsaszín csapat') {
    return '#FF69B4';
  } else if (team === 'Szürke csapat') {
    return '#808080';
  } else if (team === 'Zöld csapat') {
    return '#008000';
  } else if (team === 'Barna csapat') {
    return '#6F4E37';
  }
}
